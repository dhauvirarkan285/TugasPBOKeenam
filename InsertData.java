    private void aksiTombol() {
        // Tombol Simpan
        btnSimpan.addActionListener(evt -> {
            // validasi kosong
            if (txtKodeKabupaten.getText().trim().isEmpty() ||
                txtNamaKabupaten.getText().trim().isEmpty() ||
                txtIbukota.getText().trim().isEmpty() ||
                txtLuasWilayah.getText().trim().isEmpty() ||
                txtJumlahPenduduk.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
                return;
            }

            // validasi numeric untuk luas
            String luasStr = txtLuasWilayah.getText().trim().replace(",", ".");
            try {
                Double.parseDouble(luasStr);
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Luas wilayah harus berupa angka desimal (contoh: 1234.56)");
                txtLuasWilayah.requestFocus();
                return;
            }

            // validasi numeric untuk penduduk
            String jumlahStr = txtJumlahPenduduk.getText().trim().replace(".", "").replace(",", "");
            try {
                Long.parseLong(jumlahStr);
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Jumlah penduduk harus berupa angka bulat (contoh: 1000000)");
                txtJumlahPenduduk.requestFocus();
                return;
            }

            // kalau semua valid â†’ simpan data
            isSimpan = true;
            dispose();
        });

        // Tombol Batal
        btnBatal.addActionListener(evt -> {
            isSimpan = false;
            dispose();
        });
    }

    // Getter data kabupaten untuk dikirim ke MainClass
    public String[] getDataKabupaten() {
        if (!isSimpan) return null; // jika user batal
        return new String[]{
            txtKodeKabupaten.getText().trim(),
            txtNamaKabupaten.getText().trim(),
            txtIbukota.getText().trim(),
            txtLuasWilayah.getText().trim(),
            txtJumlahPenduduk.getText().trim()
        };
    }
    
